package org.ukiuni.opendataja4j.entity;

public class DatasetInformation {
	private String id;
	private String datasetName;
	private String statsDataId;
	private String statCode;
	private String statName;
	private String governmentCode;
	private String governmentName;
	private String statisticsName;
	private String title;
	private String titleNo;
	private String publicState;
	private String cellsNumber;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getDatasetName() {
		return datasetName;
	}

	public void setDatasetName(String datasetName) {
		this.datasetName = datasetName;
	}

	public String getStatsDataId() {
		return statsDataId;
	}

	public void setStatsDataId(String statsDataId) {
		this.statsDataId = statsDataId;
	}

	public String getStatCode() {
		return statCode;
	}

	public void setStatCode(String statCode) {
		this.statCode = statCode;
	}

	public String getStatName() {
		return statName;
	}

	public void setStatName(String statName) {
		this.statName = statName;
	}

	public String getGovernmentCode() {
		return governmentCode;
	}

	public void setGovernmentCode(String governmentCode) {
		this.governmentCode = governmentCode;
	}

	public String getGovernmentName() {
		return governmentName;
	}

	public void setGovernmentName(String governmentName) {
		this.governmentName = governmentName;
	}

	public String getStatisticsName() {
		return statisticsName;
	}

	public void setStatisticsName(String statisticsName) {
		this.statisticsName = statisticsName;
	}

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public String getPublicState() {
		return publicState;
	}

	public void setPublicState(String publicState) {
		this.publicState = publicState;
	}

	public String getCellsNumber() {
		return cellsNumber;
	}

	public void setCellsNumber(String cellsNumber) {
		this.cellsNumber = cellsNumber;
	}

	public String getTitleNo() {
		return titleNo;
	}

	public void setTitleNo(String titleNo) {
		this.titleNo = titleNo;
	}

}
